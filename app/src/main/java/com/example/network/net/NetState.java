package com.example.network.net;

/**
 *     NET_NO       : 没有网络
 *     NET_2G       : 2G
 *     NET_3G       : 3G
 *     NET_4G       : 4G
 *     NET_4G_plus  : 4G+
 *     NET_WIFI     : wifi
 *     NET_UNKNOWN  : 无法识别网络
 */
public enum NetState {
    NET_NO,
    NET_2G,
    NET_3G,
    NET_4G,
    NET_4G_plus,
    NET_WIFI,
    NET_UNKNOWN
}
